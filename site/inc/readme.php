      <h1>What is VRStoryGram</h1>
      <p>VRStoryGram is a project that supports communities on the front lines of environmental
         change tell their stories using oral histories and virtual reality so that they can be shared
          openly with students, local officials, and policymakers, so we all learn more about
           what it means to sustain vibrant lives on a changing planet.</p>

      <h1>Why our work is important</h1>
      <ul>
        <li>Local environmental changes are not told from the perspective of the communities who are affected</li>
        <li>Students have a hard time grasping how the changing climate is affecting them personally</li>
        <li>There is no product that is easy to use and lets communities and students tell their own personal stories in a virtual world</li>
      </ul>

      <h1>Our Solution</h1>
      <p>VRStoryGram will adapt a VR content management system (CMS) to collect,
         store, and analyze all VR content in a centralized location. The open
          source product we are starting with is IdeaSpaceVR.</p>
      <h4>Our goal is to:</h4>
      <ul>
        <li>Allow for 360 degree images and video to be uploaded onto the system</li>
        <li>Offer drag-and-drop editing capabilities</li>
        <li>Manage all created content within the platform</li>
        <li>Publish VR experiences from the solution</li>
      </ul>

